@RestController
@RequestMapping("/items")
public class ItemController {

    @GetMapping("/item/{id}")
    public Item getItemById(@PathVariable Integer id, HttpServletRequest request) {
        System.err.println(request.getRemoteAddr());
        Item item = new Item();
        item.setId(id);
        item.setCategory("计生用品");
        item.setName("打气筒");
        item.setPrice(10000);
        return item;
    }

}
